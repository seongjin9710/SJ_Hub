package com.spring.biz.vo;

public class StuGradeVO {
	
	private int gradePk;
	private String stuTtCode;
	private int gradeNum;
	private int stuNum;
	private String lecTtCode;
	private String stuName;
	
	public int getGradePk() {
		return gradePk;
	}
	public void setGradePk(int gradePk) {
		this.gradePk = gradePk;
	}
	public String getStuTtCode() {
		return stuTtCode;
	}
	public void setStuTtCode(String stuTtCode) {
		this.stuTtCode = stuTtCode;
	}
	public int getGradeNum() {
		return gradeNum;
	}
	public void setGradeNum(int gradeNum) {
		this.gradeNum = gradeNum;
	}
	public int getStuNum() {
		return stuNum;
	}
	public void setStuNum(int stuNum) {
		this.stuNum = stuNum;
	}
	public String getLecTtCode() {
		return lecTtCode;
	}
	public void setLecTtCode(String lecTtCode) {
		this.lecTtCode = lecTtCode;
	}
	public String getStuName() {
		return stuName;
	}
	public void setStuName(String stuName) {
		this.stuName = stuName;
	}
	
	
	

}
